/*小目标出现*/
function smallshow(id)
{
  var opacity=0;
  var show = setInterval(function() {
    opacity += 0.05;
    if (opacity <=1) 
    {
      document.getElementById(id).style.opacity = opacity;
    } 
    else 
    {
      clearInterval(show);
    }
  },60);  
}

/*大目标出现*/
function bigshow(id)
{
  var opacity1=0;
  setTimeout(function(){
    var show2 = setInterval(function() {
      opacity1 += 0.05;
      if (opacity1 <=1) 
      {
        document.getElementById(id).style.opacity = opacity1;
      } 
      else 
      {
        clearInterval(show2);
      }
    },60);  
  },2000)
}

